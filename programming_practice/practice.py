# find the sum of numbers from 0 to 4

total = 0 
for num in range (5):
    total = total + num 
print(total)

# the total is equal to zero initally 
# for every number in the range of 5
# the total is added to that range 
# total = 0 + 0 = 0 
# total = 0 + 1 = 1 
# total = 1 + 2 = 3
# total = 3 + 3 = 6
# total = 6 + 4 = 10

